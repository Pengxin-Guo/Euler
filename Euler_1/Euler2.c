#include<stdio.h>
int main(){
    int a=1,b=2,t,num=0;
    while(b<=4000000){
        if((b&1)==0){
            num+=b;
        }
        t=a+b;
        a=b;
        b=t;
    }
    printf("%d",num);
    return 0;
}
